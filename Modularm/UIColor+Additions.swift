import UIKit

extension UIColor
{
   class func highlightedOptionButtonColor() -> UIColor
   {
      return UIColor(red: 250/255.0, green: 250/255.0, blue: 250/255.0, alpha: 1)
   }

   class func normalOptionButtonColor() -> UIColor
   {
      return UIColor(white: 0.94, alpha: 1)
   }

   class func lipstickRedColor() -> UIColor
   {
      return UIColor(red: 190/255.0, green: 15/255.0, blue: 60/255.0, alpha: 1)
   }
}
